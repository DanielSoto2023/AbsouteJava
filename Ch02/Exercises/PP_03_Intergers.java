

import java.util.Scanner;

public class PP_03_Intergers {
 public static void main(String[] args) {
    Scanner keyboard = new Scanner(System.in);
    System.out.println("Enter two intergers");
    int FirstInterger = keyboard.nextInt();
    int SecondInterger = keyboard.nextInt();
    int Sum = FirstInterger + SecondInterger;
    int Product = FirstInterger * SecondInterger;
    int Difference = FirstInterger - SecondInterger;
    System.out.println("The Sum is: " + Sum);
    System.out.println("The Product is: " + Product);
    System.out.println("The Difference is: " + Difference);




    
 }   
}
